				<p>O Brasil vem assistindo a um crescimento cada vez maior do número de pessoas que, motivadas por questões de caráter pessoal ou social, se envolvem em ações de natureza voluntária, em busca não de reconhecimento, mas de realização pessoal, através de pequenas ou grandes tarefas, necessariamente, construtiva e positiva. O País resgata, assim, o sentimento da solidariedade. O sucesso das ações de voluntariado tem mostrado o potencial e o vigor do esforço dessas pessoas que, geralmente no anonimato, se envolvem com questões sociais que exigem dedicação e constância, em ocupações que têm muitas formas.</p>				
				<p>O diversificado trabalho voluntário é sempre importante para a sociedade. Seja em atividades pontuais, como as campanhas do agasalho, de limpeza e preservação de locais públicos ou as festas de Natal; seja através da rotina em associações de bairro, organizações sem fins lucrativos ou no apoio às escolas, realizando leitura para deficientes visuais ou reforço pedagógico com pessoas portadoras de outras necessidades; seja contando histórias para crianças fora da escola ou proporcionando-lhes momentos lúdicos e educativos; seja, ainda, emprestando conhecimentos administrativos e gerenciais à condução das entidades.</p>
